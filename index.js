function sky( sky ){
    if (sky === 'Clear'){
        return 'Despejado'
    } else if (sky === 'Clouds'){
        return 'Nublado'
    } else if (sky === 'Snow'){
        return 'Nieve'
    } else if (sky === 'Rain'){
        return 'Lluvia'
    }
}

function wind( wind ){
    let speed = wind.speed * 3.6
    return speed
}

function upper(string){
    return string[0].toUpperCase() + string.substring(1)
}